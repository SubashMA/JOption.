    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Customer c = new Customer ();
        c.setCustomerName(jTextField1.getText());
        c.setVehicleNumber(Integer.parseInt(jTextField2.getText()));
        c.setVehicleType(jTextField3.getText());
        c.setContactNumber(Integer.parseInt(jTextField4.getText()));
        c.setEmail(jTextField5.getText());
        c.setAddress(jTextField6.getText());
        
   Java_Project_JOptionPUEntityManager.getTransaction().begin();
   Java_Project_JOptionPUEntityManager.persist(c);
   Java_Project_JOptionPUEntityManager.getTransaction().commit();
   
   JOptionPane.showMessageDialog(rootPane, "Record is inserted");
   
   customerList.clear();
   customerList=customerQuery.getResultList();
   updateTable(customerList);
   
   
    }//GEN-LAST:event_jButton1ActionPerformed

